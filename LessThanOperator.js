let x = 5;
let y = 4;

if (x < y) {
  console.log("x is less than y");
} else {
  console.log("x is not less than y");
}

console.log("x < y", x < y);

if (x <= y) {
  console.log("x is less than or equal to y");
} else {
  console.log("x is not less than or equal to y");
}

console.log("x <= y", x <= y);
